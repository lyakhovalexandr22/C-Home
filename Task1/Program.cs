/* Задайте значения M и N. Напишите программу, которая выведет все натуральные числа в промежутке от M до N.
 Использовать рекурсию, не использовать циклы.
*/






    
    
        // Задаем значения M и N
        int M = 1;
        int N = 10;
    {
        // Выводим натуральные числа в заданном промежутке с помощью рекурсивной функции
        PrintNaturalNumbers(M, N);
    }

    static void PrintNaturalNumbers(int current, int end)
    {
        // Базовый случай: если текущее число больше конечного, завершаем рекурсию
        if (current > end)
            return;

        // Выводим текущее число
        Console.Write(current + " ");

        // Рекурсивно вызываем функцию для следующего числа
        PrintNaturalNumbers(current + 1, end);
    }





